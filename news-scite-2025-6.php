        <h2>Executive Summary</h2>

        <p>Seafood fraud remains a persistent global concern, encompassing mislabelling of species, origin misrepresentation, transshipment concealment, and substitution with lower-value species or aquaculture products. Recent work reinforces that mislabelling rates vary by market, species, and supply chain complexity, with consumer-level testing and DNA barcoding being central tools for detection.</p>

        <p>Advances in molecular forensics — DNA barcoding, DNA metabarcoding, and shelf-stable assays — enable rapid, high-throughput identification of mislabelled seafood across raw and processed products, supporting enforcement and consumer protection efforts.</p>

        <p>Market-level studies highlight geographic hot spots for seafood fraud, including major export/import hubs. Several recent reports document persistent mislabelling in cuisines and restaurant supply chains, underscoring the need for integrated policy responses, supply-chain traceability, and port-of-origin verification.</p>

        <p>Policy and governance developments emphasise strengthening traceability requirements, independent auditing, and consumer-facing labelling schemes, though practical implementation remains uneven globally.</p>

        <h2>Key Claims with Supporting Citations</h2>

        <div class="claim-block">
          <span class="claim-num">Claim 1</span>
          <h3>The scope of seafood fraud includes species mislabelling, geographic/origin misrepresentation, and substitution with lower-value products.</h3>
          <p>This triad of fraud dimensions is consistently observed across recent studies and reviews, establishing a broad framework for detection and enforcement efforts (Harada et al., 2023).</p>
          <p class="evidence">Systematic and scoping reviews detail mislabelling rates across markets and highlight common substitution patterns — e.g. higher-value species replaced with cheaper substitutes (Harada et al., 2023).</p>
          <p class="evidence">Enforcement-oriented reports describe mechanisms by which fraud arises in cross-border supply chains and points of sale (restaurants, markets, and processing plants).</p>
        </div>

        <div class="claim-block">
          <span class="claim-num">Claim 2</span>
          <h3>Molecular forensics (DNA-based methods) are central to detecting seafood fraud in processed and raw products, with DNA barcoding and metabarcoding enabling species-level identification even in degraded or mixed products.</h3>
          <p>These methods are increasingly integrated into regulatory and industry surveillance programs.</p>
          <p class="evidence">Recent methodological literature and case studies show high reliability of DNA barcoding for identifying seafood species in markets and products, including processed items where morphological identification is impossible.</p>
          <p class="evidence">Metabarcoding approaches allow simultaneous identification of multiple species within mixed or processed products, expanding capability beyond single-species tests.</p>
          <p class="evidence">Several national and international programs adopt DNA-based traceability workflows as part of seafood fraud investigations and compliance checks.</p>
        </div>

        <div class="claim-block">
          <span class="claim-num">Claim 3</span>
          <h3>Geographic variation: mislabelling prevalence varies by country/region, with certain hubs showing higher rates due to dense trade networks, regulatory gaps, and consumer demand pressures.</h3>
          <p>This heterogeneity necessitates targeted enforcement and region-specific policy design.</p>
          <p class="evidence">Market surveys and shipment-level studies report higher incidence in some large import-export corridors, often where there is high demand for premium species or limited supply transparency.</p>
          <p class="evidence">Comparative analyses across regions reveal differences in regulatory stringency and enforcement capacity that influence fraud prevalence.</p>
        </div>

        <div class="claim-block">
          <span class="claim-num">Claim 4</span>
          <h3>Policy and governance responses are evolving to require traceability, origin labelling, and independent verification, but practical implementation is uneven and depends on supply-chain complexity and capacity.</h3>
          <p>Harmonisation of standards and investment in testing infrastructure are repeatedly identified as critical levers.</p>
          <p class="evidence">Policy analyses discuss the push for enhanced traceability — e.g. origin declarations, catch documentation, and chain-of-custody records — and the need for credible third-party audits.</p>
          <p class="evidence">Evaluations of regulatory frameworks show variable adoption of DNA-based verification in enforcement programs and call for scalable, cost-effective testing solutions.</p>
        </div>

        <h2>Synthesis by Theme</h2>

        <h3>Detection Technologies</h3>
        <p>The strongest convergence across sources is the role of DNA-based methods as a robust, scalable means to verify species identity and provenance in seafood supply chains. DNA barcoding remains a standard reference method, while DNA metabarcoding expands detection to multi-species products, which is crucial for processed foods and mixed products. These methods complement traditional labelling checks and port controls, enabling more precise enforcement.</p>

        <h3>Market Realities</h3>
        <p>Fraud is not uniform; mislabelling rates differ by species, product form (fresh, frozen, canned, processed), and market structure (retail vs. restaurant vs. wholesale). The highest fraud vulnerability tends to occur where supply chains are long, opaque, and driven by high-value species or limited supply — underscoring the need for targeted surveillance in these segments.</p>

        <h3>Policy Implications</h3>
        <p>Strengthening supply-chain traceability, improving labelling accuracy, and supporting independent verification are repeatedly identified as essential. The gap between policy intent and real-world implementation is a recurring theme, with calls for capacity-building, standardised methods, and cost-effective deployment of molecular methods to routine inspections.</p>

        <h2>Recommended Research Agenda (Last Year)</h2>

        <ol>
          <li><strong>Expand systematic surveillance</strong> to include region-specific hot spots identified in recent market studies, with a focus on both fresh and processed products, and include restaurant supply chains as they are increasingly implicated in mislabelling.</li>
          <li><strong>Accelerate deployment of DNA-based verification</strong> in routine inspections, including the development of field-deployable assays and portable sequencing workflows to reduce turnaround times for enforcement decisions.</li>
          <li><strong>Invest in cross-border collaboration</strong> for traceability data sharing, standardisation of labelling requirements, and harmonisation of testing protocols to reduce loopholes in the supply chain.</li>
        </ol>

        <div class="nuance-box">
          <div class="callout-title">Notes on Nuance and Potential Disagreements</div>
          <p>Some studies may report varying mislabelling rates across regions, and differences in methodology — sampling design, species coverage, and product types — can influence reported prevalence. It is important to interpret prevalence estimates in light of sampling frames and laboratory methods used (Harada et al., 2023).</p>
